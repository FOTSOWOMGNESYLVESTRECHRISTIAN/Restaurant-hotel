<script>
    // Initialiser AOS si disponible
    if (typeof AOS !== 'undefined') {
        AOS.init({
            duration: 800,
            easing: 'ease-in-out',
            once: true,
            offset: 100
        });
    }

    // Animation des liens sociaux
    document.addEventListener('DOMContentLoaded', function() {
        const socialLinks = document.querySelectorAll('.social-link');
        socialLinks.forEach((link, index) => {
            setTimeout(() => {
                link.style.animation = 'fadeInUp 0.6s ease-out';
            }, index * 100);
        });

        // Animation des widgets au scroll
        const footerWidgets = document.querySelectorAll('.footer-widget');
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    entry.target.style.transform = 'translateY(0)';
                }
            });
        }, { threshold: 0.1 });

        footerWidgets.forEach(widget => {
            widget.style.opacity = '0';
            widget.style.transform = 'translateY(20px)';
            widget.style.transition = 'all 0.6s ease-out';
            observer.observe(widget);
        });
    });
</script>